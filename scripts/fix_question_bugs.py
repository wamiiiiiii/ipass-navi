def apply_fixes(json_path, answer_fixes=None, explanation_fixes=None):
    """指定ファイルに修正を適用する"""
    with open(json_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    items = data.get("questions") if isinstance(data, dict) else data
    fixed_answers = 0
    fixed_explanations = 0

    for q in items:
        qid = q.get("question_id") or q.get("id")
        if answer_fixes and qid in answer_fixes:
            old = q.get("correct_answer")
            new = answer_fixes[qid]
            if old != new:
                q["correct_answer"] = new
                fixed_answers += 1
                print(f"  [{qid}] correct_answer: {old} → {new}")
        if explanation_fixes and qid in explanation_fixes:
            q["explanation"] = explanation_fixes[qid]
            fixed_explanations += 1
            print(f"  [{qid}] explanation 更新")

    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    return fixed_answers, fixed_explanations


